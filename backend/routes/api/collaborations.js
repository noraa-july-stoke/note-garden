router.delete("/:id(\\d+)", requireAuth, async (req, res) => {
  const { id } = req.params;
  const collaboration = await Collaboration.findByPk(id);
  if (!collaboration) return res.sendStatus(404);
  await collaboration.destroy();
  res.status(204).json();
});

