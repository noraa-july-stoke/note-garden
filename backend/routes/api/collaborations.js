router.post("/", requireAuth, async (req, res) => {
  const { collaboratorId, noteId } = req.body;
  const textNote = true;
  const userId = req.user.id;
  const collaboration = await Collaboration.create({
    userId,
    collaboratorId,
    noteId,
    textNote,
  });
  res.json({ collaboration });
});

